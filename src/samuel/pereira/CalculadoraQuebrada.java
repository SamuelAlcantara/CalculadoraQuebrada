    private void buttonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonIgualActionPerformed
        // TODO add your handling code here:
        number2 = Double.parseDouble(tela.getText());
        if("dividir".equals(sinal)){
            result = number1/number2;
            tela.setText(String.valueOf(result));
        }
        if("multiplicar".equals(sinal)){
            result = number1*number2;
            tela.setText(String.valueOf(result));
        }
        if(result == -1){
            botao1.setText("--");
        }
        if(result == 11){
            botao2.setText("--");
        }
        if(result == 20){
            botao3.setText("--");
        }
        if(result == 26){
            botao4.setText("--");
        }
        if(result == 72){
            botao5.setText("--");
        }
        if(result == 210){
            botao6.setText("--");
        }
        if(result == 396){
            botao7.setText("--");
        }
        if(result == 864){
            botao8.setText("--");
        }
    }//GEN-LAST:event_buttonIgualActionPerformed

